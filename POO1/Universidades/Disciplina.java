package ListaUm.Universidades;

public class Disciplina {

    private String nome;
    private int modulos;


    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }


    public int getModulos() {
        return modulos;
    }
    public void setModulos(int modulos) {
        this.modulos = modulos;
    }
}
